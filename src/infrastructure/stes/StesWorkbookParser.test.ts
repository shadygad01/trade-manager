interface ObsInput {
  id?: string;
  doc?: string;
  type?: string;
  ticker?: Cell;
  companyName?: string;
  date?: Cell;
  time?: Cell;
  quantity?: Cell;
  price?: Cell;
  gross?: Cell;
  fees?: Cell;
  taxes?: Cell;
  dividendAmount?: Cell;
  currency?: string;
  transactionReference?: string;
  confidence?: string;
  notes?: string;
}

function obsRow(o: ObsInput): Cell[] {
  return [
    o.id ?? null,
    o.doc ?? null,
    o.type ?? null,
    o.ticker ?? null,
    o.companyName ?? null,
    null,
    null,
    o.date ?? null,
    o.time ?? null,
    o.quantity ?? null,
    o.price ?? null,
    o.gross ?? null,
    o.fees ?? null,
    o.taxes ?? null,
    null,
    o.dividendAmount ?? null,
    null,
    o.currency ?? null,
    null,
    null,
    o.transactionReference ?? null,
    null,
    null,
    o.confidence ?? null,
    o.notes ?? null,
  ];
}

function buildWorkbook(options: {
  schemaName?: string | null;
  schemaVersion?: string | null;
  documents?: Cell[][];
  observations?: Cell[][];
  omitSheets?: string[];
}): ArrayBuffer {
  const wb = XLSX.utils.book_new();
  const omit = new Set(options.omitSheets ?? []);
  if (!omit.has("Metadata")) {
    const metadata: Cell[][] = [
      ["Key", "Value"],
      ...(options.schemaName === null ? [] : [["Schema Name", options.schemaName ?? STES_SCHEMA_NAME] as Cell[]]),
      ...(options.schemaVersion === null ? [] : [["Schema Version", options.schemaVersion ?? "1.1"] as Cell[]]),
      ["Generated By", "test"],
    ];
    XLSX.utils.book_append_sheet(wb, XLSX.utils.aoa_to_sheet(metadata), "Metadata");
  }
  if (!omit.has("Documents")) {
    XLSX.utils.book_append_sheet(wb, XLSX.utils.aoa_to_sheet([DOC_HEADERS, ...(options.documents ?? [])]), "Documents");
  }
  if (!omit.has("Observations")) {
    XLSX.utils.book_append_sheet(wb, XLSX.utils.aoa_to_sheet([OBS_HEADERS, ...(options.observations ?? [])]), "Observations");
  }
  return XLSX.write(wb, { type: "array", bookType: "xlsx" }) as ArrayBuffer;
}

const doc = (id: string, type: string, fileName = `${id}.pdf`): Cell[] => [id, fileName, type, "Thndr", null, null, null, 1, "NATIVE_TEXT", null];

let originalTrackingStart: string;
