export type TranslationVars = Record<string, string | number>;
type Entry = string | ((vars: TranslationVars) => string);
export interface Dict {
  [key: string]: Entry | Dict;
}

function interpolate(template: string, vars?: TranslationVars): string {
  if (!vars) return template;
  return template.replace(/\{\{(\w+)\}\}/g, (match, name: string) => {
    const value = vars[name];
    return value === undefined ? match : String(value);
  });
}

function lookup(dict: Dict, path: string[]): Entry | undefined {
  let node: Dict | Entry = dict;
  for (const segment of path) {
    if (typeof node !== "object" || node === null) return undefined;
    node = (node as Dict)[segment] as Dict | Entry;
    if (node === undefined) return undefined;
  }
  return node as Entry;
}

export function translate(dicts: Record<Language, Dict>, lang: Language, key: string, vars?: TranslationVars): string {
  const path = key.split(".");
  const entry = lookup(dicts[lang], path) ?? lookup(dicts.en, path);
  if (entry === undefined) return key;
  const resolved = typeof entry === "function" ? entry(vars ?? {}) : entry;
  return interpolate(resolved, vars);
}

const enPlural = (n: number, singular: string, plural: string) => (n === 1 ? singular : plural);
/** Arabic plural: 0/1/2 each get their own dedicated form; 3+ uses the plural form (Egyptian colloquial simplification of MSA's dual/paucal system, standard practice for app UI copy). */
const arPlural = (n: number, zero: string, one: string, two: string, plural: string) =>
  n === 0 ? zero : n === 1 ? one : n === 2 ? two : plural;

